void main(List<String> args) {
  // int dan double
  final int num1 = 10;
  final double num2 = 10.5;

  print('number 1 : ${num1}');
  print('number 2 : ${num2}');

  // num
  final num num3 = 100;
  final num num4 = 12.102;

  print('number 3 : $num3');
  print('number 4 : $num4');
  print('number 3 + number 4 : ${num3 + num4}');
  /// num bisa menampung pecahan atau bilangan bulat

  
}
