void main(List<String> args) {
  // penugasan
  String fullName = "Rizal";
  fullName += " Fauzi";

  print(fullName);

  int a = 10;
  a -= 5;

  print(a);

  // increment dan decrement
  int i = 1;
  // i++; // i = i + 1;
  // i--; // i = i - 1;
  int j = i++; // j = i, i++
  int k = ++i; // k = (++i)
  print(j);
  print(k);
}
